print("--------------Forward----------------")
for n in range(6):
    print(n)

print("---------------------------------------------")

for n in range(1,6):
    print(n)

print("---------------------------------------------")

for n in range(1,6,2):
    print(n)

print("------------Reverse-------------------")

for n in range(10,0,-1):
    print(n)

print("---------------------------------------------")

for n in range(10,0,-2):
    print(n)